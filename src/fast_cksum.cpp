/**

Calculates a CRC32 checksum that is consistent with the GNU cksum utility.

This code is based on Stephan Brumme's Fast CRC32, with a modifications
to the algorithm and lookup table to match GNU cksum.

See:
https://create.stephan-brumme.com/crc32/
https://pubs.opengroup.org/onlinepubs/009695399/utilities/cksum.html

*/

#include "fast_cksum.h"

// define endianess and some integer data types
// defines __BYTE_ORDER as __LITTLE_ENDIAN or __BIG_ENDIAN
#include <sys/param.h>

// The standard CRC32 polynomial, in cksum's bit-reversed convention
const uint32_t polynomial = 0x04C11DB7;

/// swap endianess
static inline uint32_t swap(uint32_t x)
{
#if defined(__GNUC__) || defined(__clang__)
  return __builtin_bswap32(x);
#else
  #error "No builtin byte swap?"
  return (x >> 24) |
        ((x >>  8) & 0x0000FF00) |
        ((x <<  8) & 0x00FF0000) |
         (x << 24);
#endif
}

const int MaxSlice = 32;

/// forward declaration, table is at the end of this file
extern const uint32_t CksumLookup[MaxSlice][256]; // extern is needed to keep compiler happy

/// Main entry point; selects one of the chunking algorithms below
uint32_t crc32_fast(const void* data, size_t length)
{
  return crc32_fast_16bytes(data, length);
  //return crc32_fast_32bytes_prefetch (data, length);
  //return crc32_fast_16bytes_prefetch (data, length);
}

/// Main entry point for computing a partial result
uint32_t crc32_fast_partial(const void* data, size_t length, uint32_t previousCrc32)
{
  return crc32_fast_16bytes_partial(data, length, previousCrc32);
  //return crc32_fast_32bytes_prefetch_partial(data, length, previousCrc32);
  //return crc32_fast_16bytes_prefetch_partial(data, length, previousCrc32);
}

// Main entry point for finalization.
// Having accumulated one or more partial results into a CRC,
// now fold in the CRC of the length itself, as per cksum
uint32_t crc32_fast_finalize(size_t total_length, uint32_t previousCrc32){
  previousCrc32 = ~previousCrc32;
  do{
    previousCrc32 = (previousCrc32 << 8) ^ swap(CksumLookup[0][(previousCrc32 >> 24) ^ (total_length & 0xFF)]);
  } while(total_length >>= 8);

  return ~previousCrc32;
}

